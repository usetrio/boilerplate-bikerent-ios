import Foundation
import UIKit

extension UIColor {
    
    class var appLightGray: UIColor {
        return UIColor(red: 237/255, green: 237/255, blue: 237/255, alpha: 1)
    }
    
    class var mainBlue: UIColor {
        return UIColor(red: 42/255, green: 72/255, blue: 201/255, alpha: 1)
    }
    
    class var mainYellow: UIColor {
        return UIColor(red: 248/255, green: 216/255, blue: 130/255, alpha: 1)
    }
    
}

